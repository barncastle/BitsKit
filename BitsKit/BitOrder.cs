namespace BitsKit;

/// <summary>
/// An enumeration listing the possible directions of bits in a unit
/// </summary>
public enum BitOrder
{
    /// <summary>
    /// "Low-Order" or "Right-Most" bit
    /// </summary>
    LeastSignificant,
    /// <summary>
    /// "High-Order" or "Left-Most" bit
    /// </summary>
    MostSignificant
}
